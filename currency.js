const dropdownlist = document.querySelectorAll("form select");
const fromcurrency = document.querySelector("#form ");
const tocurrency = document.querySelector("#to ");
const Get_Exchange = document.querySelector("form button");
const amount = document.querySelector("form input");
const  Get_Exchange_text= document.querySelector("form .exchange-rate");
const  Get_Exchange_reverse= document.querySelector(".icon");

for (let index = 0; index < dropdownlist.length; index++) {
    
    for (const currency_code in country_list) {
       let selected =
            index == 0
            ? currency_code == "RUB"
                ? "selected"
                : ""
            : currency_code == "INR"
            ? "selected"
            : "";

        let option = `<option value="${currency_code}" ${selected}>${currency_code}</option>`;   
        dropdownlist[index].insertAdjacentHTML("beforeend",option);
    }
    dropdownlist[index].addEventListener("change",(e)=>{
        loadflag(e.target);
    });
}

function loadflag(flag){
    for (const code in country_list) {
        if(code==flag.value){
            let imgtag =  flag.parentElement.querySelector("img");
            imgtag.src = `https://flagcdn.com/48x36/${country_list[code].toLowerCase()}.png`;
        }
    }
}

Get_Exchange.addEventListener("click",(e)=>{
    e.preventDefault();
     let value = amount.value.trim();
    if(value ==''||value=='0')
    {
        alert(`Please enter valid amount`);
        return;
    }
    getexchangerate(value);
});
Get_Exchange.addEventListener("keydown",(e)=>{
    if(e.key=="enter")
    {
        e.preventDefault();
        let value = amount.value.trim();
        if(value ==''||value=='0')
        {
            alert(`Please enter valid amount`);
            return;
        }
        getexchangerate(value);
    }
});

function getexchangerate(value){
    Get_Exchange_text.innerText = "Getting exchange rate...";
    const apikey = `6380ff663445f76176dfc700`;
    const url = `https://v6.exchangerate-api.com/v6/${apikey}/latest/${fromcurrency.value}`;
    fetch(url)
    .then((response)=> response.json())
    .then((data) =>{
        console.log(data);
        let rate = data.conversion_rates[tocurrency.value];
        let totalrate = (value*rate).toFixed(2);
        Get_Exchange_text.innerHTML =`${value} ${fromcurrency.value} = ${totalrate} ${tocurrency.value}`; 
        
    })
    .catch( ()=>{
        Get_Exchange_text.innerHTML = `Something went wrong`;
    });
  
}

